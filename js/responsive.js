function Show() {
    document.getElementById('menu-responsive').style.display = 'block';
    document.getElementById('btn').style.display = 'none';
    document.getElementById('btn-x').style.display = 'block';
}
function Hide() {
    document.getElementById('menu-responsive').style.display = 'none';
    document.getElementById('btn').style.display = 'block';
    document.getElementById('btn-x').style.display = 'none';
}